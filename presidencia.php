  <p>Isaú Chacon, presidente da UGT-DF</p>

<img src="img/fotochacon.jpg" width="153" height="295" style="float:left; padding-right:10px; padding-bottom:10px;"><p>O presidente da UGT - União Geral dos Trabalhadores no Distrito Federal, Isaú Chacon, também é o atual presidente do Sindicato dos Securitários do Distrito Federal, entidade na qual atua desde 1991, tendo ocupado vários cargos até assumir a Presidência.</p>

  <p>Graduado em Direito e tendo experiencia na área sindical vem fazendo um belo trabalho à frente dessas organizações</p>

  <p>Como dirigente sindical, Isaú Chacon defende um novo rumo para o sindicalismo brasileiro, incorporando as idéias de cidadania, ética e inovação, e agregando ao movimento sindical os excluídos do mercado de trabalho: os desempregados, os trabalhadores na informalidade e os brasileiros que estão à margem do processo produtivo.</p>

