<p><STRONG>A UNIÃO GERAL DOS TRABALHADORES ‑  UGT </STRONG>é uma associação civil com prazo indeterminado de duração, com  jurisdição em todo território brasileiro e representação em todos os Estados. É  entidade de grau máximo de representação sindical e tem a finalidade de  coordenar, representar e defender os direitos e os interesses dos  trabalhadores.</p>



  <p>É uma  organização sindical formada a partir da unificação das centrais sindicais  Confederação Geral dos Trabalhadores - CGT, Social Democracia Sindical – SDS,  Central Autônoma de Trabalhadores - CAT e de um amplo grupo de sindicatos  independentes. A UGT foi fundada em 19 de julho de 2007, durante o Congresso  Nacional de Trabalhadores que se realizou nos dias 19, 20 e 21/07/07 em São  Paulo, Capital, e reuniu mais de 3.400 delegados, representando 623 entidades  sindicais de todo o país e mais de 5 milhões de trabalhadores,  ocasião em que foi eleita a 1ª executiva  nacional  para um mandato de 4 anos tendo  como presidente Nacional, o sindicalista <STRONG>Ricardo Patah</STRONG>, presidente do  Sindicato dos Comerciários de São Paulo.</p>



  <p>Constituída  para defender os trabalhadores brasileiros através de um movimento sindical  amplo, cidadão, ético, solidário, independente, democrático e inovador. A UGT  defende a unidade no sindicalismo e o direito à livre associação e organização,  visando a construção de um projeto social pacífico, justo e democrático,  centrado no ser humano, capaz de oferecer respostas e propostas aos problemas  nacionais. </p>



