O fundo de pensão da Petrobrás deverá apresentar em 2015 o terceiro déficit seguido e terá de ter um plano em 2016. Ano passado, a Petros registrou déficit de R$ 3,8 bilhões, acumulando R$ 6,2 bilhões em dois anos. 
