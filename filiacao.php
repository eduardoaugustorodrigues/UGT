<strong>Por que se filiar a UGT?</strong>
<p>Atualmente a UGT é uma das Centrais que mais crescem no Brasil, contando com mais de 1.400 entidades sindicais filiadas, a luta dos trabalhadores por direitos sociais, por aumentos salariais, conquistas pontuais ou ampliação constante, passa pela unidade e integração das entidades sindicais a uma organização nacional. A filiação de Sindicato, Federação ou Confederação a um central é mais do que uma forma de organização, é uma necessidade nos dias de hoje e um gesto de coragem e compromisso com um país igual para todos.</p>
<p><strong>Vantagens de pertencer aos quadros da UGT:</strong></p>
<p>1-    Fortalecimento da sua categoria e da luta geral dos trabalhadores;</p>
<p>2-    Maior visibilidade de seu sindicato;</p>
<p>3-    Maior espaço para seu sindicato interferir nas decisões que podem melhorar a vida dos trabalhadores;</p>
<p>4-    Maior comprometimento da base com o desenvolvimento econômico e social do País;</p>
<p>5-    Apoio da Central as entidades sindicais filiadas;</p>
<p><strong>Como se filiar:</strong></p>
<p>Preencher a ficha de filiação <strong><a href="img/Ficha filiação UGT.pdf" target="_blank">clicando aqui</a></strong> e encaminhar para o <a href="mailto:ugtdf@ugtdf.org.br" target="_blank">ugtdf@ugtdf.org.br</a> ou entregar em nossa sede no endereço que consta abaixo<strong>.</strong><strong></strong></p>
<p>OBS: Para efetuar a filiação, é necessário certificado digital. Caso o sindicato não possua,<strong> <a href="http://csbbrasil.org.br/orientacoes-para-a-certificacao-digital/" target="_blank">clique aqui</a></strong></p>
<p><strong>Após ter o Certificado Digital instalado no computador, siga os seguintes passos:</strong></p>
<p>1- Acesse o link <strong><a href="http://portal.mte.gov.br/cnes/atualizacao-de-dados-perene-sd.htm" target="_blank">http://portal.mte.gov.br/cnes/atualizacao-de-dados-perene-sd.htm</a> </strong>;<br>
  2- Clique em &ldquo;sindicato&rdquo; no lado direito de seu monitor;<br>
  3- Coloque o CNPJ de seu sindicato;<br>
  4- Em &ldquo;central sindical&rdquo;, escolha a opção União Geral dos Trabalhadores e clique na opção gravar;<br>
  5- Clique na opção &ldquo;Transmitir&rdquo;;<br>
  6- Imprima a SD de filiação, assine as duas vias, <strong>anexe a ata da Diretoria da Entidade (registrada em cartório) que aprovou a filiação</strong> e protocole na Agência do Ministério do Trabalho mais próxima do seu sindicato.</p>
