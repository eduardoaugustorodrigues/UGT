Nem a chuva que tomou conta da manhã de sábado conteve a festa deste 08 de março, Dia Internacional da Mulher. A União Geral dos Trabalhadores (UGT), junto às demais centrais sindicais e movimentos sociais, esteve presente na Marcha que reuniu cerca de 2 mil pessoas, que, do vão livre do Masp, na Av. Paulista, caminharam até a Praça Roosevelt, mostrando que muito ainda há a ser feito para conquistar os direitos da mulher.
<br>
<br>
<br>
Por mais avanço nas políticas públicas voltadas para as mulheres, por mais Justiça, por direito a creches, cuidar dos filhos, pelo fim de todas as formas de violência, respeito, reconhecimento. São alguns dos inúmeros motivos que todos, unidos, se reuniram na marcha da mulheres. Uma festa democrática, onde o rap, movimentos de teatro, humanistas e de qualquer tipo expressão puderam deixar seus recados.
<br>
<br>
<br>
"Hoje, 08 de março, nós queremos que toda igualdade seja respeitada. Queremos que todas as mulheres tenham seus direitos, porque a categoria das domésticas ainda nao tem os mesmos direitos das demais categorias. E é importante a cada ano aumentarmos a quantidade de mulheres e homens, lado a lado, para que posssamos ter uma sociedade justa e igualitária. E é a partir desses movimentos que nós vamos, no futuro, ter uma sociedade igual”, defende Cleonice de Souza, secretária da Saúde e Segurança no Trabalho da UGT.
<br>
<br>
<br>
Há 3 anos que a UGT participa desse movimento. ”Infelizmete a gente ainda está na rua reivindicando direitos que já poderíamos estar comemorando, mas não é um dia de comemoração e sim de reflexão! Ainda duas mulheres por dia são assassinadas, vítimas do machismo, da lesbofobia e é isso que a gente está nas ruas: com chuva ou sem chuva, que as mulheres, e homens também, estão nas ruas buscando mais igualdade, aprovação do PL 6653, que desde 2009 tramita na Câmara e no Senado e que ninguém põe pra frente porque infelizmente nós ainda somos minoria”, adverte Joyce Ribeiro, assessora da Secretaria da Mulher da UGT. 
<br>
<br>
<br>
E juntando forças para poder buscar e reivindicar mais direitos e liberdade, homens como Vitor Dias, ator do grupo de teatro Levante Mulher, esteve na caminhada para dar um basta em tanta desigualdade. “As diferenças começam dentro de casa, com questões domésticas, desde a minha mãe cobrar mais da minha irmã do que de mim, por exemplo. E são pequenas coisas que acabam causando até a morte de muitas mulheres. E vir para um movimento é muito importante, porque não luta só pelas mulheres, mas por um todo: pela liberdade, igualdade social de todos”, defende.
<br>
<br>
<br>
Para Isabel Kausz, diretora e responsável pelo Departamento da Mulher do Sindicato dos Comerciários de SP, filiado à UGT, ainda é preciso muita coisa. ”Precisamos ter políticas públicas voltadas para as mulheres. Mas politicas publicas no sentido transversal mesmo, onde todas as secretarias têm que se unir e construir um plano específico, porque quando a gente fala hoje da violência à mulher, ela é muito ampla, não é so agressão, assédio no local de trabalho: é você não ter uma creche decente pra deixar teu filho e ir trabalhar, é você procurer um posto de saúde e não conseguir ser atendida. Essas são formas de violências. Não adianta você atacar somente a justice. Se todas as secretarias se unirem, com certeza vamos ter grandes mudanças”.
<br>
<br>
<br>
A mulher precisa ter direito ao seu corpo, ser melhor atendida no sistema de saúde. E a violência às mulheres imigrantes também não fica de fora. A chilena Andrea Caravantes, residente no Brasil há 4 anos, faz parte da Equipe de base Warmis – Mulheres Imigrantes, que representa as convergências das culturas humanistas. “A gente está trabalhando principalmente com mulheres bolivianas no tema da violência obstétrica e os direitos das mulheres imigrantes também. Não tem respeito”, relata. “Eu sou chilena, branca, tem sido mais fácil, mas para as  mulheres bolivianas é bem mais difícil o acesso à saúde. Nos postos de saude há muitos maus tratos, na hora do parto elas não são respeitadas”, continua, pontuando a dificuldade quando o assunto também é documentacao e direitos cíveis.
<br>
<br>
<br>
Cremilda Bastos Cravo, suplente da diretoria do Sindicato dos Comerciários, alerta que ainda há muito a melhorar. “Há 20 anos a situação era bem mais complicada. Já conseguimos avançar muito e ainda há muito o que fazer para que nós, mulheres, cheguemos ali, igual aos homens, que é o nosso desejo. E a caminhada é maravilhosa, o Dia Internacional da Mulher, é todo dia e vamos parar com aquela historia de que lugar de mulher é na cozinha, porque lugar de mulher é em todo o lugar!, festeja.
<br>
<br>
<br>
Que nas próximas marchas, o PL da Igualdade esteja aprovado, que mulheres não sejam mais separadas de seus filhos ou que não sejam mais estatísticas de violência nem desvalorizados seus direitos. Feliz dia a dia para todas as mulheres. Por mulheres mais felizes e atuantes!
