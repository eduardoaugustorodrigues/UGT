<span style="font-size:18px">Posição das Organizações da Sociedade Civil na Temática do Aquecimento Global</span>

<img src="img/rfp.jpg" width="885" height="496">
<P align="justify">
As mudanças climáticas tem sido uma temática em discussões por todo o globo terrestre, os 

impactos são evidentes e os seus efeitos já saíram das teorias e estão sendo vividos por várias 

comunidades, o processo de aquecimento que vem sendo divulgado pela mídia mundial é de 

fato um alerta para os seres viventes nesse planeta, evidências de que o globo terrestre está 

realmente sofrendo uma ação natural de aquecimento para o período proposto, os tratados 

internacionais e os acordos são muito tímidos no sentido de criar um ambiente global de 

sustentabilidade, e enquanto isso, pequenos atos vão se alastrando pelo mundo com muita 

ideologia de muita significância, pois num contexto global é necessário mais que pequenos 

focos de movimento, mas uma ação orquestrada da sociedade civil organizada, aliadas a entes 

estatais representando de forma substancial uma preocupação latente e um ação mais 

ostensiva para os problemas ambientais, não só aqueles ligados ao aquecimento global que 

mesmo não tendo sido completamente atribuído cientificamente a ação humana, merece 

atenção. O mapeamento da cadeia de degradação, conhecimento desde os fatos menores 

ligados ao tema e a sucessão de elementos que culminam no aquecimento global é 

extremamente importante, pois só através do conhecimento conseguiremos organizar ações 

de forma a coordenar esforços de forma eficaz, visando não só mitigar os efeitos da ação 

predatória do homem na terra, mas planejar mesmo que no longo prazo, a sua erradicação e 

assim poder trabalhar na correção desses efeitos, que por si só trarão um trabalho com 

esforços heroicos. É necessário ter consciência da realidade que nos cerca, passos iniciais são 

importantes, a participação em discussões locais, preparativas, comunitárias a criação de 

incentivos, premiações concursos, a formação de agentes de mudança, até a construção de 

grandes defensores, lideranças políticas, sociais e ambientais, comprometidas com a causa, 

são de vital importância para o sucesso das conferências, e fóruns, todos estes com vistas ao 

evento de âmbito mundial conhecido como COP a Conferência Mundial do Clima, onde de fato 

as diretrizes maiores devem ser reconhecidas. Acontece que, um trabalho de base deve ser 

feito, levar em consideração aspectos comuns e territoriais, questões intrínsecas de cada 

pedaço de chão são essenciais para a construção de políticas sustentáveis, contudo não se 

pode admitir sem antes criticar, correntes que atribuem esses fenômenos a ação humana, pois 

essa relação não pode ser cientificamente comprovada, e servem para criar um ambiente de 

alarmismo desnecessário, isso nos leva a crer que alguém está de alguma forma criando um 

ambiente de especulação, precisamos sim de um mundo mais sustentável, mas precisamos 

cuidar que para essa evolução, o alarmismo não exista em prol da especulação que não 

interessa a grande massa e não cria resultados sustentáveis.
<br>
<br>
Os resultados das últimas conferências são um termômetro, também indicativos fortes

de que ainda não estamos preparados para lidar com o assunto de maneira efetiva, os 

problemas elencados em Varsóvia como a divulgação de um acordo fraco e pouco ambicioso 

para os avanços necessários, mostram que a propagação da rede de informações sobre a 

sustentabilidade não chegaram a sensibilizar comunidades, para que gerem um número 

relevante de formadores de opinião, capazes de convencer o aparelho estatal a criar políticas 

nesse sentido, o que se reflete nos fóruns e na própria conferência COP 19. A saída das ONGs 

entre outros eventos negativos foram determinantes para os insucessos desse evento, a 

criação de novas diretrizes para substituição do protocolo de Kioto não foram possíveis e o 

resultado da COP, frustrante. Observemos que o trabalho de base é muito importante, e nesse 

contexto a preparação necessária além da participação dos fóruns exige a formação de lideres 

atentos para essa temática, capaz de não só se articular com a sociedade civil, mais também 

politicamente com os governos. O resultado dessas movimentações são na verdade o inicio 

de uma expectativa de sucesso. Dentro do Brasil podemos citar que quando o assunto é a 

preservação da mata atlântica, existe um hiato entre o que se fala e o que se pratica, na COP 

19 foi divulgado um aumento de 28% no desmatamento da Amazônia legal. Fatos como esse 

estão diretamente relacionados a essa falta de articulação, onde o estado propõe medidas por 

sugestão da sociedade civil e não fiscaliza, também não são fiscalizados pela sociedade que 

propõe, e assim a falta de pessoas qualificadas para realizar não somente as negociações em 

âmbito nacional e de se relacionar em nível internacional, criando uma rede de articulação 

não somente social, mas também política. Temos de reconhecer que talvez o estado esteja 

influenciado por entes nacionais e internacionais, que não tenham interesse em reduzir seus 

lucros, por medidas sustentáveis, e mesmo quando isso representa ganhos, não conseguimos 

articular isso de maneira a evitar a degradação ambiental.
<br>
<br>
Os movimentos sociais brasileiros tem se feito presente em várias discussões, a formação de

membros ativos vem sendo priorizados pela sociedade civil de uma forma geral, ongs, 

sindicatos, centrais sindicais, dentre elas a UGT, União Geral dos Trabalhadores onde as 

discussões sobre o assunto são capitaneadas por sindicalistas comprometidos com a causa, 

que através de conhecimento e articulação leva a Central Sindical ao patamar em que se 

encontra, de ente ativo em prol da sustentabilidade, instituindo também o comitê de 

sustentabilidade, que abriga não só sindicalistas comprometidos com a causa ambiental mas 

também ONGs associadas com a fiscalização e acompanhamento dos parques, a preservação 

de outros biomas presentes no território brasileiro, e o envolvimento da comunidade 

impedindo a degradação ambiental pode ser amplificado, e aplicado em todo país, tirando do 

campo da discussão todas essas medidas e levando para a prática de ações efetivas e 

sustentáveis, a relação estreita das regionais da UGT e a UGT Nacional em face do comitê 

nacional de sustentabilidade tem sido de grande importância, e essa produtividade será 

ampliada, a medida que a UGT reconhece os esforços e investe na qualificação de seus 

membros, para que a sociedade civil tenha representantes a altura dos desafios que são 

apresentados, pois as propostas hoje defendidas em âmbito regional e nacional pela UGT, 

muito bem representada refletem nas conferências maiores como objetivos práticos para um 

mundo mais sustentável. É essencial a preocupação com a formação pois a representatividade 

não é nada quando não está aliada a propostas factíveis, pois só através da prática da 

transformação podemos, criar um ambiente de divulgação e aprendizado, e interferir com 

propriedade nas discussões a que nos propomos a fazer. Os exemplos que tiramos das ações 

regionais, mostram claramente que é necessário a participação de todos no resultado, pois, 

somente pessoas preparadas para os desafios das conferências, poderão alterar as histórias de 

insucesso que estigmatizam as conferências internacionais do meio ambiente. A participação 

das entidades que defendem o trabalhador no contexto do aquecimento global, estão visando 

onde e como esses vão trabalhar sem destruir o planeta, sob os temas da transição justa, 

trabalho decente, e empregos verdes, mais inclusão, menos degradação e sempre sob o 

prisma da valorização do trabalhador, sem é claro, esquecer do meio ambiente.
